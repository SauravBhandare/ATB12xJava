package ex_09_Switch;

public class Lab_100_Interview9 {
    public static void main(String[] args) {

        char ch= 'F';
        switch (ch)
        {
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
            case 'C':
                System.out.println("67");
                break;
            default:
                System.out.println("None of the above");
                break;

        }


    }



}
