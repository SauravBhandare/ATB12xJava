package ex_04_Operators;

public class Lab_040_Operators_Relational {

    public static void main(String[] args) {

        /*
        > --> greater than
        >= --> greater than equal to
        < --> Less than
        <= --> Less than equal to
        == --> comparisonal (equal to equal to)
        != --> not equal to
         */

        // Relational operator will always result boolean output

        int a= 10;
        int b =20;

        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
