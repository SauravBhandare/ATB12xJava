package ex_04_Operators;

public class Lab_041_Operators_Logical {

    public static void main(String[] args) {


        // And Condition
        boolean b5 = true;
        boolean b6 = false;

        System.out.println(b5 && b6);  // false

        // OR conditon

        System.out.println(b5 || b6);  // true


      // Not condition
        boolean b1 = true;
        System.out.println(!b1);   //false


    }
}
