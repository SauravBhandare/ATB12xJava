package ex_04_Operators;

public class Lab_037_Operators_Arithmetic {

    public static void main(String[] args) {

        // Arithmetic Operators
         /*
         + --> Additiom
         - --> Subtraction
         * --> Multiplication
         / --> DIvison   (output is quotient)
         %  --> Modulus  (output is remainder)
          */

        int a =10 ;
        int b = 3;
        float c = 4.50f;


        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(a/c);
        System.out.println(a%c);

        System.out.println("a+b");


    }
}
