package ex_03_Literals;

public class Lab_35_Literals_Non_Primitive {

    public static void main(String[] args) {

        //Primitive datatype  Defined by java
        // Min max fixed size
        byte b = 10;  // 1 byte 8 bits
        char c = 30; // 4 byte 32 bits

        // Non Primitive data type   Defined by user
        // Min Max not fixed size
       // 8 byte 64 bits

        String str ="SauravBhandare";
        System.out.println(str);


    }
}
