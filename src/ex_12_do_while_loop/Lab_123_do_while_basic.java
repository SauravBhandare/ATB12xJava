package ex_12_do_while_loop;

public class Lab_123_do_while_basic {

    public static void main(String[] args) {

        // In do while loop we print the statement once and then check condition
        int i = 1;

        do{
            System.out.println("Number is");
            System.out.println(i);
            i++;
        }
        while(i<=5);

    }
}
