package ex_10_for_loop;

public class Lab_107_for_loop_long_float {
    public static void main(String[] args) {

        for (long l = 1l ; l<10l ; l++)
        {
            System.out.println(l);
        }
        for (float f = 0.1f ; f<0.9f ; f++)
        {
            System.out.println(f);
        }
    }


}
