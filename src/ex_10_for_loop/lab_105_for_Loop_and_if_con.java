package ex_10_for_loop;

public class lab_105_for_Loop_and_if_con {

    public static void main(String[] args) {

        for (int age =0 ; age<=21 ; age++)
        {
            if (age>=18)
            {
                System.out.println("I am Adult");
            }
            else
            {
                System.out.println("I am Child");
            }
        }
    }
}
